package GraphGeneratorProject.Helpers;

import java.awt.Color;

public class Colors {
	
	private static final Color
		DARK_GREY_COLOR = Color.decode("#d8dadb"),
		LIGHT_GREY_COLOR = Color.decode("#ecedee"),
		RED_COLOR = Color.RED,
		WHITE_COLOR = Color.WHITE,
		BLACK_COLOR = Color.BLACK;

	public static Color getDarkGreyColor() {
		return DARK_GREY_COLOR;
	}

	public static Color getLightGreyColor() {
		return LIGHT_GREY_COLOR;
	}

	public static Color getRedColor() {
		return RED_COLOR;
	}

	public static Color getWhiteColor() {
		return WHITE_COLOR;
	}

	public static Color getBlackColor() {
		return BLACK_COLOR;
	}

}